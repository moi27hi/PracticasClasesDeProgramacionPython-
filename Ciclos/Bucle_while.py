# Ciclo while.

i = 1
print('*** \n Para incrementar el valor de la variable**')
while i < 5:
    print(f'El valor del bucle es: {i}.')
    
    i +=1
    
# Decrementar el bucle. 
print('*** \n Para Disminuir el valor de la variable**')
while i >= -5:
    print(f'El valor del bucle es: {i}.')
    i -= 1
    
    
    




