

for i in range(5):
    print(f"El valor del bucle es: {i}")  # i es la variable iterable. 
    


print('\n*** El bucle inicia desde el 3')
for i in range(3, 7):
    print(f"El valor del bucle es: {i}")  # i es la variable iterable
    

print('\n*** El bucle inicia desde el 3 y va de 3 en 3')

for i in range(3,33,3):
    print(f"El valor del bucle es: {i}")  # i es la variable iterable

print('fin del bucle')


print('\n***Ciclo for con decrementos')

for i in range(-3,-33,-3):
    print(f"El valor del bucle es: {i}")  # i es la variable iterable

print('fin del bucle')



    
    
